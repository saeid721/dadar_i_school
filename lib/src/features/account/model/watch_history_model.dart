class WatchHistoryModel {
  int? code;
  String? status;
  String? message;
  List<WatchHistoryData>? data;

  WatchHistoryModel({this.code, this.status, this.message, this.data});

  WatchHistoryModel.fromJson(Map<String, dynamic> json) {
    code = json['code'];
    status = json['status'];
    message = json['message'];
    if (json['data'] != null) {
      data = <WatchHistoryData>[];
      json['data'].forEach((v) {
        data!.add(WatchHistoryData.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['code'] = code;
    data['status'] = status;
    data['message'] = message;
    if (this.data != null) {
      data['data'] = this.data!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class WatchHistoryData {
  int? id;
  int? subscriberId;
  int? movieId;
  int? episodeId;
  String? videoProgress;
  String? createdAt;
  String? updatedAt;
  Movie? movie;
  String? episode;

  WatchHistoryData(
      {this.id,
        this.subscriberId,
        this.movieId,
        this.episodeId,
        this.videoProgress,
        this.createdAt,
        this.updatedAt,
        this.movie,
        this.episode});

  WatchHistoryData.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    subscriberId = json['subscriber_id'];
    movieId = json['movie_id'];
    episodeId = json['episode_id'];
    videoProgress = json['video_progress'];
    createdAt = json['created_at'];
    updatedAt = json['updated_at'];
    movie = json['movie'] != null ? Movie.fromJson(json['movie']) : null;
    episode = json['episode'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['id'] = id;
    data['subscriber_id'] = subscriberId;
    data['movie_id'] = movieId;
    data['episode_id'] = episodeId;
    data['video_progress'] = videoProgress;
    data['created_at'] = createdAt;
    data['updated_at'] = updatedAt;
    if (movie != null) {
      data['movie'] = movie!.toJson();
    }
    data['episode'] = episode;
    return data;
  }
}

